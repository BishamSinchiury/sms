def make_profile(org, **overrides):
    profile, _ = OrganizationProfile.objects.get_or_create(org=org)
    for k, v in overrides.items():
        setattr(profile, k, v)
    if overrides:
        profile.save()
    return profile

def make_legal(org, **overrides):
    legal, _ = OrganizationLegal.objects.get_or_create(org=org)
    for k, v in overrides.items():
        setattr(legal, k, v)
    if overrides:
        legal.save()
    return legal

def make_domain(org, domain="test.edu", is_primary=True, is_verified=True):
    return OrgDomain.objects.create(org=org, domain=domain, is_primary=is_primary, is_verified=is_verified)

def make_sub_org(org, code="sci-dept", **overrides):
    defaults = {"name": "Test Sub Org"}
    defaults.update(overrides)
    return SubOrganization.objects.create(parent_org=org, code=code, **defaults)

